#OR Operator:
#if any of the operation is True then it prints True
print(2>3 or 2>1)
print(2>3 or 2>5)


#AND Operator:
#if any condition is false then it prints false

print(2>3 and 2>5)
print(2>1 and 2>4)
print(2>1 and 2>0)

#NOT Operator:
#it converts true into false and false into true

print(not 2>3)
print(not 2>1)